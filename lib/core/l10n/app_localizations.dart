class AppLocalizationsDelegate extends LocalizationsDelegate<AppLocalizations> {
  final Locale? forced;

  const AppLocalizationsDelegate(this.forced);

  @override
  bool isSupported(Locale locale) => 
    ['ru', 'en', 'uz', 'tg', 'qya', 'os', 'uk', 'sah', 'cu'].contains(locale.languageCode);

  @override
  Future<AppLocalizations> load(Locale locale) async {
    return AppLocalizations(forced ?? AppLocalizations.forcedLocale ?? locale);
  }

  @override
  bool shouldReload(covariant AppLocalizationsDelegate old) => old.forced != forced;
}