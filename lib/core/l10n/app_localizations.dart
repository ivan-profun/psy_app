    final lang = locale.languageCode;
    final direct = _localizedValues[lang]?[key];
    if (direct != null) return direct;

    final lowerKey = key.toLowerCase();
    final lower = _localizedValues[lang]?[lowerKey];
    if (lower != null) return lower;

    final enDirect = _localizedValues['en']?[key];
    if (enDirect != null) return enDirect;

    final enLower = _localizedValues['en']?[lowerKey];
    if (enLower != null) return enLower;

    return lowerKey;
  }

